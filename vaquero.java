
public class vaquero {
	
	private String nombre;
	private int salud, daño, recarga;
	private boolean disparo, defensa, nada;

//gets and sets
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getSalud() {
		return salud;
	}
	public void setSalud(int salud) {
		this.salud = salud;
	}
	public int getDaño() {
		return daño;
	}
	public void setDaño(int daño) {
		this.daño = daño;
	}
	public int getRecarga() {
		return recarga;
	}
	public void setRecarga(int recarga) {
		this.recarga = recarga;
	}
	public boolean isDisparo() {
		return disparo;
	}
	public void setDisparo(boolean disparo) {
		this.disparo = disparo;
	}
	public boolean isDefensa() {
		return defensa;
	}
	public void setDefensa(boolean defensa) {
		this.defensa = defensa;
	}
	public boolean isNada() {
		return nada;
	}
	public void setNada(boolean nada) {
		this.nada = nada;
	}

	
	
}
